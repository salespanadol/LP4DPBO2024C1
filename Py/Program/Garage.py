class Garage:
    def __init__(self, namagarasi="", luas=""):
        self.namagarasi = namagarasi
        self.luas = luas

    def get_namagarasi(self):
        return self.namagarasi

    def set_namagarasi(self, namagarasi):
        self.namagarasi = namagarasi
    
    def get_luas(self):
        return self.luas

    def set_luas(self, luas):
        self.luas= luas